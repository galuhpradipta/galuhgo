package main

import (
	"fmt"
)

func main() {

	x := 11
	y := 5

	// aritmatic operator

	// additional
	zAdd := x + y
	fmt.Println(zAdd)

	// substract
	zSub := x - y
	fmt.Println(zSub)

	// multiplies
	zMul := x * y
	fmt.Println(zMul)

	// divides
	zDiv := x / y
	fmt.Println(zDiv)

	// modulus
	zMod := x % y
	fmt.Println(zMod)
}
